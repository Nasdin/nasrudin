//! Model Context Protocol (MCP) server for the Physics Generator.
//!
//! This crate will expose the theorem database and GA engine as an MCP tool server,
//! allowing LLM agents (Claude, etc.) to:
//!
//! - Browse and search theorems
//! - Inspect proof trees
//! - Submit conjectures for verification
//! - Monitor the genetic algorithm's progress
//! - Query dimensional analysis results
//!
//! Implementation will follow the MCP specification once the core engine is stable.
